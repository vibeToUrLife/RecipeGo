// Lightweight i18n: one flat dictionary per locale + a translate() helper used
// by both server (getT) and client (useT). Client-safe (no server-only imports)
// so the dictionary can be bundled for client components too.

export type Locale = 'en' | 'zh'
export const LOCALES: Locale[] = ['en', 'zh']
export const DEFAULT_LOCALE: Locale = 'en'

export function detectLocale(acceptLanguage: string | null | undefined): Locale {
  if (acceptLanguage && /(^|[,;\s])zh\b/i.test(acceptLanguage)) return 'zh'
  return 'en'
}

